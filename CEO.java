public class CEO {
    int rating;
    String name;
    String face;
    String companyLogo;
    String companyName;

    public CEO() {
        rating = 100;
        name = "";
        face = "";
        companyLogo = "";
        companyName = "";
    }

    public String getRating() {
        return name;
    }

    public String getName() {
        return name;
    }

    public String getFace() {
        return name;
    }

    public String getLogo() {
        return name;
    }

    public String getCompany() {
        return name;
    }

    public void setName(String param) {
        name = param;
    }

    public void setRating(int param) {
        rating = param;
    }

    public void setFace(String param) {
        face = param;
    }

    public void setLogo(String param) {
        companyLogo = param;
    }

    public void setCompany(String param) {
        companyName = param;
    }
}
